def read_t1_summary(
    paths: Sequence[Path],
    targets: Sequence[str],
    relax: bool = False,
    relax_ipr: bool = False,
) -> Dict[str, Dict[str, int]]:
    try:
        from openpyxl import load_workbook
    except ImportError as exc:
        raise RuntimeError("T1 report parsing requires openpyxl") from exc

    target_filter = set(targets)
    summary: Dict[str, Dict[str, int]] = defaultdict(lambda: {key: 0 for key in OPTICS_FEC_KEYS})

    for raw_path in paths:
        path = raw_path.expanduser()
        reject_t1_side_source_report(path, "--t1-report")
        if not path.exists():
            raise FileNotFoundError(path)
        wb = load_workbook(path, read_only=True, data_only=True)
        if "Summary" in wb.sheetnames:
            ws = wb["Summary"]
            rows = ws.iter_rows(values_only=True)
            try:
                header = [str(value).strip() if value is not None else "" for value in next(rows)]
            except StopIteration:
                continue
            dg_idx = find_column(header, ("DG", "Target"))
            rx_idx = find_column(header, ("Optics RX", "RX Optics", "RX"))
            tx_idx = find_column(header, ("Optics TX", "TX Optics", "TX"))
            prefec_idx = find_column(header, ("Pre-FEC", "Pre FEC", "PreFEC"))
            fecbin_idx = find_column(header, ("FEC Bin", "FEC BIN", "FECBin"))
            for row in rows:
                if dg_idx >= len(row) or row[dg_idx] is None:
                    continue
                raw_target = str(row[dg_idx]).strip()
                if normalize(raw_target) == "total":
                    continue
                try:
                    target = normalize_target(raw_target)
                except ValueError:
                    continue
                if target not in target_filter:
                    continue
                summary[target]["RX Optics"] += parse_int(row[rx_idx] or 0)
                summary[target]["TX Optics"] += parse_int(row[tx_idx] or 0)
                summary[target]["Pre-FEC"] += parse_int(row[prefec_idx] or 0)
                summary[target]["Fec Bin"] += parse_int(row[fecbin_idx] or 0)
            continue

        if "All" not in wb.sheetnames:
            continue
        ws = wb["All"]
        rows = ws.iter_rows(values_only=True)
        try:
            header = [str(value).strip() if value is not None else "" for value in next(rows)]
        except StopIteration:
            continue
        dg_idx = find_column(header, ("DG", "Target"))
        issue_idx = find_column(header, ("Issue Type",))
        power_idx = optional_column(header, ("Power Type",))
        for row in rows:
            if dg_idx >= len(row) or row[dg_idx] is None:
                continue
            raw_target = str(row[dg_idx]).strip()
            if normalize(raw_target) == "total":
                continue
            try:
                target = normalize_target(raw_target)
            except ValueError:
                continue
            if target not in target_filter:
                continue
            issue = normalize(row[issue_idx] if issue_idx < len(row) else "")
            power = normalize(row[power_idx] if power_idx is not None and power_idx < len(row) else "")
            row_data = row_dict(header, row)
            if issue == "optics" and power in {"rx", "tx"} and not optics_row_passes_relaxed_threshold(
                row_data,
                relax=relax,
                relax_ipr=relax_ipr,
            ):
                continue
            if issue == "optics" and power == "rx":
                summary[target]["RX Optics"] += 1
            elif issue == "optics" and power == "tx":
                summary[target]["TX Optics"] += 1
            elif issue == "prefec":
                summary[target]["Pre-FEC"] += 1
            elif issue == "fecbin":
                summary[target]["Fec Bin"] += 1

    return dict(summary)


def flatten_path_groups(groups: Sequence[Sequence[Path]] | None) -> List[Path]:
    paths: List[Path] = []
    for group in groups or []:
        paths.extend(group)
    return paths


def build_report(
    previous: Dict[str, Dict[str, int]],
    current: Dict[str, Dict[str, int]],
    targets: Sequence[str],
    t1_summary: Dict[str, Dict[str, int]] | None = None,
) -> str:
    target_rows = []
    all_targets = [*targets, TOTAL_TARGET]
    for target in all_targets:
        prev = previous[target]["Total"]
        cur = current[target]["Total"]
        target_rows.append([target, prev, cur, signed_int(cur - prev), progress(prev, cur)])

    sections = [
        "T0 <> T1:",
        "Target Total Summary",
        table(
            COMPARISON_HEADERS,
            target_rows,
            left_columns={0},
        ),
        "",
        "Issue Breakdown By Target",
    ]

    for target in targets:
        sections.extend(
            [
                "",
                target,
                table(
                    COMPARISON_HEADERS,
                    metric_rows(previous[target], current[target]),
                    left_columns={0},
                ),
            ]
        )

    sections.extend(
        [
            "",
            "Total Types Of Errors",
            table(
                COMPARISON_HEADERS,
                metric_rows(previous[TOTAL_TARGET], current[TOTAL_TARGET]),
                left_columns={0},
            ),
        ]
    )

    if t1_summary:
        sections.extend(
            [
                "",
                "T1 <> T0:",
                table(
                    ("DG", "RX Optics", "TX Optics", "FEC Bin", "Pre-FEC", "Grand Total"),
                    optics_fec_rows(t1_summary, targets),
                    left_columns={0},
                ),
            ]
        )

    return "\n".join(sections)


def read_block(label: str, sentinel: str) -> str:
    print(
        f"\nPaste {label}. Finish with a line containing only {sentinel!r}.",
        file=sys.stderr,
    )
    lines: List[str] = []
    while True:
        try:
            line = input()
        except EOFError as exc:
            raise SystemExit(f"Input ended before sentinel {sentinel!r} for {label}") from exc
        if line.strip() == sentinel:
            break
        lines.append(line)
    return "\n".join(lines)


def resolve_previous_path(path: Path | str | None) -> Path | None:
    if path is None:
        return None
    resolved = Path(path).expanduser()
    if not resolved.is_absolute():
        resolved = DEFAULT_DATA_DIR / resolved
    return resolved


def load_or_prompt(path: Path | str | None, label: str, sentinel: str, missing_ok: bool = False) -> str:
    if path:
        resolved = Path(path).expanduser()
        if missing_ok and not resolved.exists():
            return ""
        return resolved.read_text(encoding="utf-8")
    return read_block(label, sentinel)


def paths_match(left: Path, right: Path) -> bool:
    return left.expanduser().resolve() == right.expanduser().resolve()


def write_text_report(path: Path, report: str) -> Path:
    resolved = path.expanduser()
    resolved.parent.mkdir(parents=True, exist_ok=True)
    resolved.write_text(report + "\n", encoding="utf-8")
    return resolved


def write_text_outputs(report: str, output_path: Path | None, previous_path: Path | None) -> None:
    written_output = None
    if output_path:
        written_output = write_text_report(output_path, report)
        print(f"\nReport written to: {written_output}")

    if previous_path:
        if written_output is not None and paths_match(previous_path, written_output):
            written_previous = written_output
        else:
            written_previous = write_text_report(previous_path, report)
        print(f"\nPrevious report updated: {written_previous}")


def current_paths(args: argparse.Namespace) -> Dict[str, str]:
    paths: Dict[str, str] = {}

    for item in args.current or []:
        if "=" not in item:
            raise ValueError(f"Invalid --current value {item!r}; expected DG=path")
        target, path = item.split("=", 1)
        target = normalize_target(target)
        if not path:
            raise ValueError(f"Invalid --current value {item!r}; path is empty")
        paths[target] = path

    return paths



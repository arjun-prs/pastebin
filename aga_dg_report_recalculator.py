def main() -> int:
    args = parse_args()

    try:
        full_report = args.full_report or args.full_report_arg
        if args.full_report and args.full_report_arg and args.full_report != args.full_report_arg:
            raise ValueError("Use either positional full_report.xlsx or --full-report, not both")
        previous_path = resolve_previous_path(args.previous)
        previous_text_from_path = ""
        if previous_path:
            previous_text_from_path = load_or_prompt(
                previous_path,
                "the previous formatted report",
                args.sentinel,
                missing_ok=True,
            )

        requested_targets = parse_target_list(args.targets)
        if requested_targets is None:
            if not previous_path:
                raise ValueError("--targets is required unless --previous is provided")
            requested_targets = discover_targets(previous_text_from_path.splitlines())
            if not requested_targets:
                raise ValueError(
                    "Could not infer --targets from --previous; "
                    "the previous report is missing or contains no DG sections"
                )
        targets = list(requested_targets)

        if args.print_qcli_command:
            print(combined_qcli_command(targets, args.relax))
            return 0

        excel_paths = flatten_path_groups(args.excel)
        if full_report:
            excel_paths.append(full_report)
        if excel_paths:
            previous: Dict[str, Dict[str, int]]
            previous_has_content = False
            if previous_path:
                previous_text = previous_text_from_path
                previous_has_content = bool(previous_text.strip())
                if previous_has_content:
                    previous, _previous_targets = previous_current_metrics(
                        previous_text,
                        requested_targets,
                        allow_missing=True,
                    )
                else:
                    previous = {}
            else:
                previous = {}

            current, _current_targets, details = read_excel_current(
                excel_paths,
                requested_targets,
                args.relax,
                args.relax_ipr,
            )
            targets = list(requested_targets)
            if not targets:
                raise ValueError("No DG rows matched the workbook input and rack mapping")

            fill_missing_previous_from_current(previous, current, targets)

            previous[TOTAL_TARGET] = sum_metrics(*(previous[target] for target in targets))
            current[TOTAL_TARGET] = sum_metrics(*(current[target] for target in targets))

            t1_summary = None
            t1_paths = flatten_path_groups(args.t1_report)
            if t1_paths and not args.no_optics_fec_summary:
                t1_summary = read_t1_summary(t1_paths, targets, args.relax, args.relax_ipr)

            report = build_report(previous, current, targets, t1_summary)
            print(report)

            write_text_outputs(report, args.output, previous_path)
            if args.output_excel:
                write_excel_report(args.output_excel, current, targets, details)
                print(f"\nExcel report written to: {args.output_excel.expanduser()}")

            auto_excel = bool(full_report) and not args.no_auto_excel
            default_dir = (
                full_report.expanduser().parent
                if full_report
                else DEFAULT_DATA_DIR
            )
            output_timestamp = datetime.now()

            if args.t1_output_excel or auto_excel:
                t1_output = args.t1_output_excel or default_t1_t0_output_path(
                    default_dir,
                    targets,
                    output_timestamp,
                )
                written_t1_output = write_t1_t0_excel_report(t1_output, targets, details)
                print(f"\nT1 <> T0 Excel report written to: {written_t1_output}")

            if args.combined_fec_output_excel or auto_excel:
                combined_fec_output = (
                    args.combined_fec_output_excel
                    or default_combined_fec_output_path(default_dir, output_timestamp)
                )
                written_combined_fec_output = write_combined_fec_by_dg_workbook(
                    combined_fec_output,
                    targets,
                    details,
                )
                print(
                    "\nCombined FEC by DG Excel report written to: "
                    f"{written_combined_fec_output}"
                )

            return 0

        if previous_path:
            previous_text = previous_text_from_path
        else:
            previous_text = load_or_prompt(
                previous_path,
                "the previous formatted report",
                args.sentinel,
                missing_ok=True,
            )
        first_report = not previous_text.strip()
        if first_report:
            previous = {}
        else:
            previous, _previous_targets = previous_current_metrics(
                previous_text,
                requested_targets,
                allow_missing=True,
            )
        targets = list(requested_targets)
        paths = current_paths(args)

        current: Dict[str, Dict[str, int]] = {}
        warnings: List[str] = []
        for position, target in enumerate(targets, start=1):
            text = load_or_prompt(
                paths.get(target),
                f"the current {target} output ({position} of {len(targets)})",
                args.sentinel,
            )
            current[target], target_warnings = parse_rack_rollup(text, target)
            warnings.extend(target_warnings)

        fill_missing_previous_from_current(previous, current, targets)

        previous[TOTAL_TARGET] = sum_metrics(*(previous[target] for target in targets))
        current[TOTAL_TARGET] = sum_metrics(*(current[target] for target in targets))
        t1_summary = optics_fec_counts(args, targets)

        report = build_report(previous, current, targets, t1_summary)
        print(report)

        write_text_outputs(report, args.output, previous_path)

        if warnings and not args.no_warnings:
            print("\nWarnings:", file=sys.stderr)
            for warning in warnings:
                print(f"- {warning}", file=sys.stderr)

    except Exception as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1

    return 0


if __name__ == "__main__":
    raise SystemExit(main())

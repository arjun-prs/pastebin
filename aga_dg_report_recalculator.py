def racks_for_targets(targets: Sequence[str]) -> List[str]:
    racks: List[str] = []
    for target in targets:
        if target not in DG_RACKS:
            raise ValueError(f"No rack mapping exists for {target}")
        for rack in DG_RACKS[target]:
            if rack not in racks:
                racks.append(rack)
    return racks


def qcli_instance_for_target(target: str) -> int:
    return ATOM_QCLI_INSTANCE if target in ATOM_INSTANCE_TARGETS else DEFAULT_QCLI_INSTANCE


def qcli_command_for_targets(targets: Sequence[str], instance: int, relax: bool = False) -> str:
    dg_numbers: List[str] = []
    for target in targets:
        if target not in DG_RACKS:
            raise ValueError(f"No rack mapping exists for {target}")
        dg_numbers.append(str(int(target.removeprefix("DG"))))
    dg_list = ",".join(dg_numbers)
    relax_flag = " --optics-relax" if relax else ""
    return (
        f"qcli hc-summary --region aga --building 5 --instance {instance} --state deployed "
        f"--spectrum --customtag test_spectrum_fec_ber --failures-only --filter_pg --t1-reports --nvidia-linkflap-clear --slack{relax_flag} --deployment-group {dg_list}"
    )


def combined_qcli_command(targets: Sequence[str], relax: bool = False) -> str:
    targets_by_instance: Dict[int, List[str]] = {}
    for target in targets:
        instance = qcli_instance_for_target(target)
        targets_by_instance.setdefault(instance, []).append(target)
    return "\n".join(
        qcli_command_for_targets(instance_targets, instance, relax)
        for instance, instance_targets in targets_by_instance.items()
    )


def normalize(value: object) -> str:
    return re.sub(r"[^a-z0-9]+", "", str(value).strip().lower())


def t1_side_source_report_kind(path: Path) -> str | None:
    compact_name = " ".join(path.name.split())
    for prefix, kind in T1_SIDE_SOURCE_PREFIXES:
        if compact_name.startswith(prefix):
            return kind
    return None


def reject_t1_side_source_report(path: Path, option_name: str) -> None:
    report_kind = t1_side_source_report_kind(path)
    if report_kind is None:
        return
    raise ValueError(
        f"{path.name} is a T1-side {report_kind} source full_report. "
        f"Do not pass these files to {option_name} in aga_dg_report_recalculator.py. "
        "Use the combined T0 qcli full_report for this script; compact T1-side "
        "RX/Pre-FEC/FEC-Bin files with aga52_final_ppforall.py instead."
    )


def parse_int(value: object) -> int:
    text = str(value).replace(",", "").strip()
    match = re.search(r"[-+]?\d+", text)
    if not match:
        raise ValueError(f"Expected an integer value, got {value!r}")
    return int(match.group(0))


def parse_float_value(value: object) -> float | None:
    if value is None:
        return None
    if isinstance(value, (int, float)):
        return float(value)
    text = str(value).replace(",", "").strip()
    if not text:
        return None
    match = FLOAT_RE.search(text)
    if not match:
        return None
    try:
        return float(match.group(0))
    except ValueError:
        return None


def pre_fec_ber_over_threshold(value: object) -> bool:
    parsed = parse_float_value(value)
    return parsed is not None and parsed > PRE_FEC_BER_THRESHOLD


def lock_status_has_fec_bin_7_to_15(value: object) -> bool:
    text = str(value or "")
    matched_explicit_count = False
    for match in FEC_BIN_COUNT_RE.finditer(text):
        matched_explicit_count = True
        bin_num = int(match.group(1))
        count = int(match.group(2))
        if FEC_BIN_MIN <= bin_num <= FEC_BIN_MAX and count > 0:
            return True
    if matched_explicit_count:
        return False

    return any(
        FEC_BIN_MIN <= int(match.group(1)) <= FEC_BIN_MAX
        for match in FEC_BIN_RE.finditer(text)
    )


def is_lldp_interface_down_row(header: Sequence[str], row: Sequence[object]) -> bool:
    active_interface_idx = optional_column(
        header,
        ("Act. Interface", "Active Interface", "Interface Status"),
    )
    if active_interface_idx is None or active_interface_idx >= len(row):
        return False
    return normalize(row[active_interface_idx]) == "interfacedown"


def split_pipe_row(line: str) -> List[str] | None:
    stripped = line.strip()
    if not stripped.startswith("|") or not stripped.endswith("|"):
        return None
    return [cell.strip() for cell in stripped.strip("|").split("|")]


def is_border(line: str) -> bool:
    stripped = line.strip()
    return bool(stripped) and set(stripped) <= {"+", "-"}


def metric_name(raw: str) -> str | None:
    aliases = {
        "lldp": "LLDP",
        "interface": "Interface",
        "interfacedown": "Interface",
        "opticstxrx": "Optics TX/RX",
        "optics": "Optics TX/RX",
        "prefec": "Pre Fec",
        "prefecber": "Pre Fec",
        "fecbin": "Fec Bin",
        "fecbinissues": "Fec Bin",
        "combinedfec": "Combined FEC",
        "fec": "Combined FEC",
        "total": "Total",
    }
    return aliases.get(normalize(raw))


def find_column(header: Sequence[str], candidates: Iterable[str]) -> int:
    normalized = [normalize(col) for col in header]
    for candidate in candidates:
        candidate_norm = normalize(candidate)
        if candidate_norm in normalized:
            return normalized.index(candidate_norm)
    raise ValueError(f"Missing required column. Header was: {header}")


def optional_column(header: Sequence[str], candidates: Iterable[str]) -> int | None:
    try:
        return find_column(header, candidates)
    except ValueError:
        return None


def first_metric_table_after(lines: Sequence[str], start_idx: int) -> Dict[str, int]:
    for idx in range(start_idx, len(lines)):
        row = split_pipe_row(lines[idx])
        if not row:
            continue

        try:
            current_idx = find_column(row, ("Current",))
            first_row = idx + 1
        except ValueError:
            current_idx = 2
            first_row = idx

        parsed: Dict[str, int] = {}

        for row_line in lines[first_row:]:
            if is_border(row_line):
                continue

            row = split_pipe_row(row_line)
            if row is None:
                if parsed:
                    break
                continue

            name = metric_name(row[0])
            if name and current_idx < len(row):
                parsed[name] = parse_int(row[current_idx])

        if parsed:
            return {metric: parsed.get(metric, 0) for metric in METRICS}

    return {}


def normalize_target(raw: str) -> str:
    text = str(raw).strip().upper()
    match = re.fullmatch(r"(?:DG|PG)?\s*(\d+)", text)
    if not match:
        raise ValueError(f"Invalid DG target {raw!r}; expected values like DG1 or DG4")
    return f"DG{int(match.group(1))}"


def parse_target_list(raw: str | None) -> List[str] | None:
    if not raw:
        return None

    targets: List[str] = []
    for item in raw.split(","):
        item = item.strip()
        if not item:
            continue
        if item.lower() == "all":
            for target in ALL_DG_TARGETS:
                if target not in targets:
                    targets.append(target)
            continue

        target = normalize_target(item)
        if target not in targets:
            targets.append(target)

    if not targets:
        raise ValueError("No valid targets were provided")
    return targets


def discover_targets(lines: Sequence[str]) -> List[str]:
    targets: List[str] = []

    for line in lines:
        row = split_pipe_row(line)
        if row:
            candidate = row[0].strip().upper()
        else:
            candidate = line.strip().upper()

        if TARGET_RE.fullmatch(candidate) and candidate not in targets:
            targets.append(candidate)

    return targets


def previous_current_metrics(
    previous_report: str,
    targets: Sequence[str] | None = None,
    allow_missing: bool = False,
) -> Tuple[Dict[str, Dict[str, int]], List[str]]:
    lines = previous_report.splitlines()
    selected_targets = list(targets) if targets is not None else discover_targets(lines)
    if not selected_targets:
        if allow_missing:
            return {}, []
        raise ValueError("Could not find any DG metric tables in the previous report")

    result: Dict[str, Dict[str, int]] = {}

    for target in selected_targets:
        for idx, line in enumerate(lines):
            if line.strip().upper() == target:
                table = first_metric_table_after(lines, idx + 1)
                if table:
                    result[target] = table
                    break

        if target not in result and not allow_missing:
            raise ValueError(f"Could not find the {target} metric table in the previous report")

    found_targets = [target for target in selected_targets if target in result]
    result[TOTAL_TARGET] = sum_metrics(*(result[target] for target in found_targets))
    return result, selected_targets



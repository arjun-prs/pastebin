#!/usr/bin/env python3
"""
Recalculate DG progress reports from pasted text blocks or AGA full-report workbooks.

In interactive mode, the script expects:
  1. The previous formatted report, or an empty block for a first report.
  2. One current rack-level output for each DG in the previous report.
     The default AGA flow is DG1 through DG5.

It uses the previous report's Current column as the new Previous column, then
uses the rack-level DG outputs as the new Current values.

Workbook mode reads one or more T0 qcli *_full_report.xlsx files, groups rows
by the AGA5 DG rack mapping, and builds the same T0 <> T1 progress report. In
workbook mode the combined_fec_with_pp sheet is split from the row values:
  - FEC bin 7 through 15 in Lock Status counts as Fec Bin
  - Pre-FEC BER greater than 1e-7 counts as Pre Fec
  - Combined FEC remains the row count

The T1 <> T0 compact Excel artifact generated by this script is built from the
same workbook rows read by this script; it does not import or call
aga52_final_ppforall.py.

T1-side source workbooks from aga52_final_ppforall.py, such as RX power,
Pre-FEC, and FEC Bin *_full_report.xlsx files, are intentionally rejected as
input here. Compact those in aga52_final_ppforall.py instead, then optionally
pass the compact AGA5.2-DG...with-pp.xlsx workbook via --t1-report.
"""


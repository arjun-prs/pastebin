def parse_rack_rollup(current_output: str, target: str) -> Tuple[Dict[str, int], List[str]]:
    lines = current_output.splitlines()
    warnings: List[str] = []

    for idx, line in enumerate(lines):
        header = split_pipe_row(line)
        if not header or normalize(header[0]) != "rack":
            continue

        lldp_idx = find_column(header, ("LLDP",))
        interface_idx = find_column(header, ("INTERFACEDOWN", "InterfaceDown", "Interface"))
        optics_idx = find_column(header, ("OPTICS", "Optics TX/RX"))
        fec_idx = find_column(header, ("COMBINED FEC", "FEC"))
        total_idx = None
        try:
            total_idx = find_column(header, ("TOTAL", "Total"))
        except ValueError:
            pass

        totals = {
            "LLDP": 0,
            "Interface": 0,
            "Optics TX/RX": 0,
            "Pre Fec": 0,
            "Fec Bin": 0,
            "Combined FEC": 0,
            "Total": 0,
        }
        saw_rack = False

        for row_line in lines[idx + 1 :]:
            if is_border(row_line):
                continue

            row = split_pipe_row(row_line)
            if row is None:
                if saw_rack:
                    break
                continue

            rack = row[0].strip()
            rack_norm = normalize(rack)
            if rack_norm == "total":
                continue
            if not re.fullmatch(r"\d+", rack):
                if saw_rack:
                    break
                continue

            saw_rack = True
            lldp = parse_int(row[lldp_idx])
            interface = parse_int(row[interface_idx])
            optics = parse_int(row[optics_idx])
            fec = parse_int(row[fec_idx])
            calculated_total = lldp + interface + optics + fec

            if total_idx is not None and total_idx < len(row):
                printed_total = parse_int(row[total_idx])
                if printed_total != calculated_total:
                    warnings.append(
                        f"{target} rack {rack}: printed total {printed_total} "
                        f"does not match metric sum {calculated_total}"
                    )

            totals["LLDP"] += lldp
            totals["Interface"] += interface
            totals["Optics TX/RX"] += optics
            totals["Combined FEC"] += fec
            totals["Total"] += calculated_total

        if saw_rack:
            return totals, warnings

    raise ValueError(f"Could not find a rack roll-up table in the {target} input")


def optics_fec_from_values(values: Sequence[int]) -> Dict[str, int]:
    if len(values) >= 5 and values[4] == values[0] + values[1] + values[2] + values[3]:
        rx_optics, tx_optics, fec_bin, pre_fec = values[0], values[1], values[2], values[3]
    elif (
        len(values) >= 5
        and values[2] == values[0] + values[1]
        and values[4] == values[2] + values[3]
    ):
        rx_optics, tx_optics, fec_bin, pre_fec = values[0], values[1], 0, values[3]
    elif len(values) >= 4:
        rx_optics, tx_optics, fec_bin, pre_fec = values[0], values[1], values[2], values[3]
    elif len(values) >= 3:
        rx_optics, tx_optics, fec_bin, pre_fec = values[0], values[1], 0, values[2]
    else:
        raise ValueError("Expected RX optics, TX optics, FEC BIN, and Pre-FEC values")

    return {
        "RX Optics": rx_optics,
        "TX Optics": tx_optics,
        "Fec Bin": fec_bin,
        "Pre-FEC": pre_fec,
    }


def parse_optics_fec_line(line: str, targets: Sequence[str]) -> Tuple[str, Dict[str, int]] | None:
    target_set = set(targets)
    row = split_pipe_row(line)

    if row:
        target = row[0].strip().upper()
        if target == "TOTAL":
            return None
        if target in target_set:
            values = [parse_int(cell) for cell in row[1:] if re.search(r"[-+]?\d+", cell)]
            return target, optics_fec_from_values(values)
        return None

    match = TARGET_RE.search(line)
    if not match:
        return None

    target = match.group(0).upper()
    if target not in target_set:
        return None

    value_text = f"{line[:match.start()]} {line[match.end():]}"
    values = [int(value) for value in re.findall(r"[-+]?\d+", value_text.replace(",", ""))]
    return target, optics_fec_from_values(values)


def parse_optics_fec_block(text: str, targets: Sequence[str]) -> Dict[str, Dict[str, int]]:
    lines = text.splitlines()
    target_set = set(targets)
    parsed: Dict[str, Dict[str, int]] = {}

    for idx, line in enumerate(lines):
        header = split_pipe_row(line)
        if not header:
            continue

        try:
            target_idx = find_column(header, ("DG", "Target"))
            rx_idx = find_column(header, ("RX Optics", "Rx Optics", "RX"))
            tx_idx = find_column(header, ("TX Optics", "Tx Optics", "TX"))
            fecbin_idx = optional_column(header, ("FEC BIN", "Fec Bin", "FECBin"))
            prefec_idx = find_column(header, ("Pre-FEC", "Pre FEC", "PreFEC"))
        except ValueError:
            continue

        for row_line in lines[idx + 1 :]:
            if is_border(row_line):
                continue

            row = split_pipe_row(row_line)
            if row is None:
                break

            target = row[target_idx].strip().upper()
            if target == "TOTAL":
                continue
            if target not in target_set:
                continue

            parsed[target] = {
                "RX Optics": parse_int(row[rx_idx]),
                "TX Optics": parse_int(row[tx_idx]),
                "Fec Bin": parse_int(row[fecbin_idx]) if fecbin_idx is not None else 0,
                "Pre-FEC": parse_int(row[prefec_idx]),
            }

    for line in lines:
        parsed_line = parse_optics_fec_line(line, targets)
        if parsed_line:
            target, counts = parsed_line
            parsed[target] = counts

    return parsed


def read_optics_fec_counts(targets: Sequence[str], sentinel: str) -> Dict[str, Dict[str, int]]:
    print(
        "\nPaste RX optics, TX optics, FEC BIN, and Pre-FEC counts for "
        f"{', '.join(targets)}. Use rows like 'DG5 37 0 4 8', or paste a table "
        f"with DG/RX Optics/TX Optics/FEC BIN/Pre-FEC columns. Finish with {sentinel!r}; "
        f"enter {sentinel!r} immediately to skip.",
        file=sys.stderr,
    )
    lines: List[str] = []
    while True:
        try:
            line = input()
        except EOFError as exc:
            raise SystemExit(
                f"Input ended before sentinel {sentinel!r} for optics/FEC counts"
            ) from exc
        if line.strip() == sentinel:
            break
        lines.append(line)

    text = "\n".join(lines)
    if not text.strip():
        return {}
    return parse_optics_fec_block(text, targets)


def optics_fec_counts(args: argparse.Namespace, targets: Sequence[str]) -> Dict[str, Dict[str, int]]:
    if args.no_optics_fec_summary:
        return {}

    counts: Dict[str, Dict[str, int]] = {}

    if args.optics_fec_file:
        counts.update(
            parse_optics_fec_block(
                Path(args.optics_fec_file).read_text(encoding="utf-8"),
                targets,
            )
        )

    for item in args.optics_fec or []:
        parsed = parse_optics_fec_line(item, targets)
        if not parsed:
            raise ValueError(
                f"Invalid --optics-fec value {item!r}; expected DG=RX,TX,FECBIN,PRE or DG RX TX FECBIN PRE"
            )
        target, target_counts = parsed
        counts[target] = target_counts

    missing = [target for target in targets if target not in counts]
    if missing:
        counts.update(read_optics_fec_counts(missing, args.sentinel))

    if not counts:
        return {}

    missing = [target for target in targets if target not in counts]
    if missing:
        raise ValueError(
            "Missing optics/FEC counts for: "
            f"{', '.join(missing)}"
        )

    return {target: counts[target] for target in targets}


def sum_metrics(*items: Dict[str, int]) -> Dict[str, int]:
    return {metric: sum(item.get(metric, 0) for item in items) for metric in METRICS}


def progress(previous: int, current: int) -> str:
    if previous == 0:
        if current == 0:
            return "0.0%"
        return "N/A"

    pct = ((previous - current) / previous) * 100
    return f"{pct:+.1f}%"


def signed_int(value: int) -> str:
    return f"+{value}" if value > 0 else str(value)


def table(
    headers: Sequence[str] | None,
    rows: Sequence[Sequence[object]],
    left_columns: set[int],
    min_widths: Sequence[int] | None = None,
) -> str:
    string_rows = [[str(cell) for cell in row] for row in rows]
    column_count = len(headers) if headers is not None else (len(string_rows[0]) if string_rows else 0)
    widths = [
        max(
            len(str(headers[col])) if headers is not None else 0,
            min_widths[col] if min_widths is not None and col < len(min_widths) else 0,
            *(len(row[col]) for row in string_rows),
        )
        for col in range(column_count)
    ]

    border = "+" + "+".join("-" * (width + 2) for width in widths) + "+"

    def render_row(cells: Sequence[object]) -> str:
        rendered = []
        for col, cell in enumerate(cells):
            text = str(cell)
            if col in left_columns:
                rendered.append(f" {text:<{widths[col]}} ")
            else:
                rendered.append(f" {text:>{widths[col]}} ")
        return "|" + "|".join(rendered) + "|"

    output = [border]
    if headers is not None:
        output.extend([render_row(headers), border])
    output.extend(render_row(row) for row in rows)
    output.append(border)
    return "\n".join(output)


def metric_rows(previous: Dict[str, int], current: Dict[str, int]) -> List[List[object]]:
    rows: List[List[object]] = []
    for metric in METRICS:
        prev = previous.get(metric, 0)
        cur = current.get(metric, 0)
        rows.append([metric, prev, cur, signed_int(cur - prev), progress(prev, cur)])
    return rows


def optics_fec_rows(counts: Dict[str, Dict[str, int]], targets: Sequence[str]) -> List[List[object]]:
    rows: List[List[object]] = []
    totals = {key: 0 for key in OPTICS_FEC_KEYS}

    for target in targets:
        target_counts = counts.get(target, {})
        rx_optics = target_counts.get("RX Optics", 0)
        tx_optics = target_counts.get("TX Optics", 0)
        fec_bin = target_counts.get("Fec Bin", 0)
        pre_fec = target_counts.get("Pre-FEC", 0)
        grand_total = rx_optics + tx_optics + fec_bin + pre_fec

        totals["RX Optics"] += rx_optics
        totals["TX Optics"] += tx_optics
        totals["Fec Bin"] += fec_bin
        totals["Pre-FEC"] += pre_fec
        rows.append([target, rx_optics, tx_optics, fec_bin, pre_fec, grand_total])

    grand_total = (
        totals["RX Optics"]
        + totals["TX Optics"]
        + totals["Fec Bin"]
        + totals["Pre-FEC"]
    )
    rows.append(
        [
            "Total",
            totals["RX Optics"],
            totals["TX Optics"],
            totals["Fec Bin"],
            totals["Pre-FEC"],
            grand_total,
        ]
    )
    return rows


def sort_key_target(target: str) -> Tuple[int, int | str]:
    match = re.fullmatch(r"DG(\d+)", target.upper())
    if match:
        return (0, int(match.group(1)))
    if target.upper() == TOTAL_TARGET:
        return (2, target)
    return (1, target)


def rack_key(value: object) -> str | None:
    if value is None:
        return None
    text = str(value).strip()
    if not text:
        return None
    match = re.search(r"(\d{4})", text)
    return match.group(1) if match else None


def row_dict(header: Sequence[str], row: Sequence[object]) -> Dict[str, object]:
    return {
        str(name).strip() if name is not None else f"Column {idx + 1}": (
            row[idx] if idx < len(row) else None
        )
        for idx, name in enumerate(header)
    }


def target_for_excel_row(header: Sequence[str], row: Sequence[object]) -> str | None:
    rack_idx = optional_column(header, ("Rack", "DeviceA_Rack", "rack_a"))
    if rack_idx is None or rack_idx >= len(row):
        return None
    rack = rack_key(row[rack_idx])
    if not rack:
        return None
    return RACK_TO_DG.get(rack)


def empty_metric_counts() -> Dict[str, int]:
    return {metric: 0 for metric in METRICS}


def read_excel_current(
    paths: Sequence[Path],
    targets: Sequence[str] | None = None,
    relax: bool = False,
    relax_ipr: bool = False,
) -> Tuple[Dict[str, Dict[str, int]], List[str], List[Dict[str, object]]]:
    try:
        from openpyxl import load_workbook
    except ImportError as exc:
        raise RuntimeError("Workbook mode requires openpyxl") from exc

    target_filter = set(targets) if targets else None
    current: Dict[str, Dict[str, int]] = defaultdict(empty_metric_counts)
    details: List[Dict[str, object]] = []

    for raw_path in paths:
        path = raw_path.expanduser()
        reject_t1_side_source_report(path, "--excel/--full-report")
        if not path.exists():
            raise FileNotFoundError(path)
        wb = load_workbook(path, read_only=True, data_only=True)

        for sheet_name, metric in EXCEL_METRIC_SHEETS:
            if sheet_name not in wb.sheetnames:
                continue
            ws = wb[sheet_name]
            rows = ws.iter_rows(values_only=True)
            try:
                header = [str(value).strip() if value is not None else "" for value in next(rows)]
            except StopIteration:
                continue

            lock_idx = optional_column(header, ("Lock Status", "Lock_status", "LockStatus"))
            pre_fec_ber_idx = optional_column(
                header,
                (
                    "Pre-FEC BER",
                    "Pre FEC BER",
                    "Pre‑FEC BER",
                    "Pre_FEC_BER",
                    "RAW BER",
                    "RAW_BER",
                    "RAW_BER_MAX",
                ),
            )

            for row in rows:
                target = target_for_excel_row(header, row)
                if target is None:
                    continue
                if target_filter is not None and target not in target_filter:
                    continue
                if metric == "LLDP" and is_lldp_interface_down_row(header, row):
                    continue
                original = row_dict(header, row)
                if metric == "Optics TX/RX" and not optics_row_passes_relaxed_threshold(
                    original,
                    relax=relax,
                    relax_ipr=relax_ipr,
                ):
                    continue

                counts_fec_bin = 0
                counts_pre_fec = 0
                current[target][metric] += 1

                if metric == "Combined FEC":
                    lock_status = ""
                    if lock_idx is not None and lock_idx < len(row):
                        lock_status = str(row[lock_idx] or "")

                    pre_fec_ber = None
                    if pre_fec_ber_idx is not None and pre_fec_ber_idx < len(row):
                        pre_fec_ber = row[pre_fec_ber_idx]

                    if lock_status_has_fec_bin_7_to_15(lock_status):
                        current[target]["Fec Bin"] += 1
                        counts_fec_bin = 1
                    if pre_fec_ber_over_threshold(pre_fec_ber):
                        current[target]["Pre Fec"] += 1
                        counts_pre_fec = 1

                detail = {
                    "Source File": path.name,
                    "Source Sheet": sheet_name,
                    "DG": target,
                    "Issue Type": metric,
                    "Counts Fec Bin": counts_fec_bin,
                    "Counts Pre Fec": counts_pre_fec,
                }
                for key, value in original.items():
                    out_key = key if key not in detail else f"Original {key}"
                    detail[out_key] = value
                details.append(detail)

    if targets:
        selected_targets = list(targets)
        for target in selected_targets:
            current.setdefault(target, empty_metric_counts())
    else:
        selected_targets = sorted(current, key=sort_key_target)

    for values in current.values():
        values["Total"] = (
            values.get("LLDP", 0)
            + values.get("Interface", 0)
            + values.get("Optics TX/RX", 0)
            + values.get("Combined FEC", 0)
        )

    return dict(current), selected_targets, details


def unique_columns(rows: Sequence[Dict[str, object]]) -> List[str]:
    columns: List[str] = []
    for row in rows:
        for key in row:
            if key not in columns:
                columns.append(key)
    return columns


def safe_sheet_name(name: str, used: set[str]) -> str:
    cleaned = re.sub(r"[\[\]:*?/\\]", "_", name)[:31] or "Sheet"
    candidate = cleaned
    counter = 1
    while candidate in used:
        suffix = f"_{counter}"
        candidate = f"{cleaned[:31 - len(suffix)]}{suffix}"
        counter += 1
    used.add(candidate)
    return candidate



def has_pp(row: Dict[str, object]) -> bool:
    pp_a = str(row.get("PP_A") or "").strip()
    pp_z = str(row.get("PP_Z") or "").strip()
    missing = {"", "none", "nan", "pp_info_not_found"}
    return pp_a.lower() not in missing and pp_z.lower() not in missing


def optics_power_type(row: Dict[str, object]) -> str:
    power_type = normalize(row.get("Power Type", ""))
    if power_type in {"inputpower", "rx", "rxoptics"}:
        return "RX"
    if power_type in {"outputpower", "tx", "txoptics"}:
        return "TX"
    metric = normalize(row.get("Metric", ""))
    if metric in {"inputpower", "rx", "rxoptics"}:
        return "RX"
    if metric in {"outputpower", "tx", "txoptics"}:
        return "TX"
    return ""


def optics_measured_dbm(row: Dict[str, object]) -> float | None:
    candidates = (
        "Measured (dBm)",
        "Value",
        "RX power",
        "TX power",
        "Input Power",
        "Output Power",
        "Measured",
        "Power",
    )
    normalized = {normalize(key): value for key, value in row.items()}
    for column in candidates:
        value = row.get(column, normalized.get(normalize(column)))
        measured = parse_float_value(value)
        if measured is not None:
            return measured
    return None


def optics_row_passes_relaxed_threshold(
    row: Dict[str, object],
    *,
    relax: bool = False,
    relax_ipr: bool = False,
    is_ipr: bool = False,
) -> bool:
    if is_ipr:
        if not relax_ipr:
            return True
        rx_min = IPR_RELAX_RX_MIN_DBM
        tx_min = IPR_RELAX_TX_MIN_DBM
        max_value = IPR_RELAX_MAX_DBM
    else:
        if not relax:
            return True
        rx_min = NON_IPR_RELAX_RX_MIN_DBM
        tx_min = NON_IPR_RELAX_TX_MIN_DBM
        max_value = NON_IPR_RELAX_MAX_DBM

    power_type = optics_power_type(row)
    measured = optics_measured_dbm(row)
    if not power_type or measured is None:
        return True
    if power_type == "RX":
        return measured < rx_min or measured > max_value
    if power_type == "TX":
        return measured < tx_min or measured > max_value
    return True


def t1_t0_detail_rows(
    details: Sequence[Dict[str, object]],
    targets: Sequence[str],
) -> List[Dict[str, object]]:
    target_filter = set(targets)
    rows: List[Dict[str, object]] = []

    for row in details:
        target = str(row.get("DG") or "").strip().upper()
        if target not in target_filter:
            continue

        issue_type = row.get("Issue Type")
        if issue_type == "Optics TX/RX":
            power_type = optics_power_type(row)
            output = {
                "Issue Type": "Optics",
                "Power Type": power_type,
                "PP Matched": has_pp(row),
            }
            output.update(row)
            output["Issue Type"] = "Optics"
            output["Power Type"] = power_type
            rows.append(output)
            continue

        if issue_type != "Combined FEC":
            continue

        if row.get("Counts Pre Fec") == 1:
            output = {
                "Issue Type": "Pre-FEC",
                "Power Type": "",
                "PP Matched": has_pp(row),
            }
            output.update(row)
            output["Issue Type"] = "Pre-FEC"
            output["Power Type"] = ""
            rows.append(output)

        if row.get("Counts Fec Bin") == 1:
            output = {
                "Issue Type": "FEC Bin",
                "Power Type": "",
                "PP Matched": has_pp(row),
            }
            output.update(row)
            output["Issue Type"] = "FEC Bin"
            output["Power Type"] = ""
            rows.append(output)

    return rows


def write_t1_t0_excel_report(
    path: Path,
    targets: Sequence[str],
    details: Sequence[Dict[str, object]],
) -> Path:
    try:
        from openpyxl import Workbook
    except ImportError as exc:
        raise RuntimeError("Writing workbook output requires openpyxl") from exc

    output_path = timestamped_path(path.expanduser())
